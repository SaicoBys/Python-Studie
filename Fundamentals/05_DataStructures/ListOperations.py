# Descripción:
"""
Las listas en Python admiten varias operaciones que permiten modificar su contenido, como añadir o eliminar elementos.
"""

# Añadir elementos con append()
orders = ['daisies', 'periwinkle']
orders.append('tulips')
print("Órdenes actuales:", orders)

# Eliminar elementos con remove()
orders.remove('daisies')
print("Después de remover margaritas:", orders)

# Contar elementos con count()
print("Número de 'tulips' en las órdenes:", orders.count('tulips'))

